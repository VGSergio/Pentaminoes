    private void solve(){
        if(!Board.isSolving()){
            Message.setText("Solving...");
            Board.setSolving(true);
            Board.Solve(this,Board, new boolean[12], 0, 12);
            Board.setSolving(false);
//            Message.setText("Solved");
        }
    }
    
